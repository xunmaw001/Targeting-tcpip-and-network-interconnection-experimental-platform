<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            pingjiabianhao : false,
            shiyanmingcheng : false,
            banji : false,
            laoshizhanghao : false,
            laoshixingming : false,
            pingfen : false,
            pingyu : false,
            pingjiashijian : false,
            hupingxuehao : false,
            hupingxingming : false,
            crossuserid : false,
            crossrefid : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          pingjiabianhao: this.getUUID(),
          shiyanmingcheng: '',
          banji: '',
          laoshizhanghao: '',
          laoshixingming: '',
          pingfen: '',
          pingyu: '',
          pingjiashijian: '',
          hupingxuehao: '',
          hupingxingming: '',
          crossuserid: '',
          crossrefid: '',
        },


        rules: {
          pingjiabianhao: [
          ],
          shiyanmingcheng: [
          ],
          banji: [
          ],
          laoshizhanghao: [
          ],
          laoshixingming: [
          ],
          pingfen: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          pingyu: [
          ],
          pingjiashijian: [
          ],
          hupingxuehao: [
          ],
          hupingxingming: [
          ],
          crossuserid: [
          ],
          crossrefid: [
          ],
        },
      };
    },
    computed: {



    },
    components: {
    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.pingjiashijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='pingjiabianhao'){
              this.ruleForm.pingjiabianhao = obj[o];
              this.ro.pingjiabianhao = true;
              continue;
            }
            if(o=='shiyanmingcheng'){
              this.ruleForm.shiyanmingcheng = obj[o];
              this.ro.shiyanmingcheng = true;
              continue;
            }
            if(o=='banji'){
              this.ruleForm.banji = obj[o];
              this.ro.banji = true;
              continue;
            }
            if(o=='laoshizhanghao'){
              this.ruleForm.laoshizhanghao = obj[o];
              this.ro.laoshizhanghao = true;
              continue;
            }
            if(o=='laoshixingming'){
              this.ruleForm.laoshixingming = obj[o];
              this.ro.laoshixingming = true;
              continue;
            }
            if(o=='pingfen'){
              this.ruleForm.pingfen = obj[o];
              this.ro.pingfen = true;
              continue;
            }
            if(o=='pingyu'){
              this.ruleForm.pingyu = obj[o];
              this.ro.pingyu = true;
              continue;
            }
            if(o=='pingjiashijian'){
              this.ruleForm.pingjiashijian = obj[o];
              this.ro.pingjiashijian = true;
              continue;
            }
            if(o=='hupingxuehao'){
              this.ruleForm.hupingxuehao = obj[o];
              this.ro.hupingxuehao = true;
              continue;
            }
            if(o=='hupingxingming'){
              this.ruleForm.hupingxingming = obj[o];
              this.ro.hupingxingming = true;
              continue;
            }
            if(o=='crossuserid'){
              this.ruleForm.crossuserid = obj[o];
              this.ro.crossuserid = true;
              continue;
            }
            if(o=='crossrefid'){
              this.ruleForm.crossrefid = obj[o];
              this.ro.crossrefid = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.xuehao!=''&&json.xuehao) || json.xuehao==0){
                this.ruleForm.hupingxuehao = json.xuehao
            }
            if((json.xingming!=''&&json.xingming) || json.xingming==0){
                this.ruleForm.hupingxingming = json.xingming
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('xueshenghuping/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('xueshenghuping/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('xueshenghuping/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('xueshenghuping/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

